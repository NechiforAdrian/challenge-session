
public class ArithmeticOperations {

    public static void main(String[] args) {
        int a = 20;
        int b = 20;
        int c = a + b;
        System.out.println("Result addition = " + c);

        int d = 20;
        int e = 10;
        int f = d - e;
        System.out.println("Result subtraction = " + f);

        int g = 20;
        int h = 10;
        int i = g / h;
        System.out.println("Result subtraction = " + i);

        int j = 10;
        int k = 10;
        int l = j * k;
        System.out.println("Result multiply = " + l);

    }
}
